#include <unistd.h>

void	ft_putendl_fd(char *s, int fd)
{
	char	nl;

	nl = '\n';
	if (fd == -1)
		return ;
	while (*s)
	{
		write (fd, s, 1);
		s++;
	}
	write (fd, &nl, 1);
}
